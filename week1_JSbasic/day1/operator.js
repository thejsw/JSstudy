// Operator (연산자)

// 산술 연산자
console.log( 3+3 ); // 덧셈
console.log( 3-3 ); // 뺄셈
console.log( 3*3 ); // 곱셈
console.log( 3/3 ); // 나눗셈
console.log( 3%3 ); // 나머지
console.log( 3**3 ); // 거듭제곱

console.log((1+1) * 2); // 우선순위 적용

console.log( 'a' + 'b' + 'c' + 'd' + 'e' + 'f' + 'u' ) // 문자열에도 적용가능 

// 증감 연산자
var num = 0;

num++; // 하나씩 증가
console.log(num);

num--; // 하나씩 감소
console.log(num);

// 대입 연산자
var num = 10; // 오른쪽 값을 왼쪽에 대입한다.
console.log(num);

console.log( num += 1 );
console.log( num -= 1 );
console.log( num *= 2 );
console.log( num /= 2 );
console.log( num %= 2 );


